package dataType;

public class BooleanDataType {
	static boolean doYouHaveCommonSense;
	// default value is false;

	public static void main(String[] args) {
		trueAndFalse();
		System.out.println(doYouHaveCommonSense);
	}

	public static void trueAndFalse() {

		boolean doYouHaveBrain = true;
		System.out.println(doYouHaveBrain);

		boolean doYouHaveMoney = false;
		System.out.println(doYouHaveMoney);

	}

}
